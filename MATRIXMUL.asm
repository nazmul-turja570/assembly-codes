.MODEL SMALL
.STACK 100H
.DATA
A DB 1,2,3,4 ;ROW MAJOR MATRIX
B DB 5,6,7,8 ;COLUMN MAJOR MATRIX
C DB 4 DUP(0)

.CODE 
MAIN PROC
MOV AX, @DATA
MOV DS, AX

LEA BX,C
LEA DI,A
LEA SI,B

MOV CX,2
LOOP1:
PUSH BX
PUSH CX
PUSH SI
MOV CX, 2 

LOOP2:
MOV AL, [DI]
MUL [SI]
MOV DL, AL
MOV AL, [DI+1]
MUL [SI+1]
MOV [BX],DL
ADD [BX], AL
ADD SI, 2
INC BX
LOOP LOOP2 

ADD DI,2
POP SI
POP CX
POP BX
ADD BX,2
LOOP LOOP1

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN